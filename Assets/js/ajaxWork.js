//update product after submit
function updateItems() {
    var p_id = $('#p_id').val();
    var p_name = $('#p_name').val();
    var p_desc = $('#p_desc').val();
    var p_price = $('#p_price').val();
    var p_cat_id = $('#categories').val();
    var existingImage = $('#existingImage').val();
    var newImage = $('#newImage')[0].files[0];

    // Kiểm tra xem người dùng đã chọn hình ảnh mới chưa
    if (newImage === undefined) {
        alert('Vui lòng chọn hình ảnh.');
        return; // Không gửi dữ liệu nếu chưa chọn hình ảnh mới
    }

    var fd = new FormData();
    fd.append('p_id', p_id);
    fd.append('p_name', p_name);
    fd.append('p_desc', p_desc);
    fd.append('p_price', p_price);
    fd.append('p_cat_id', p_cat_id);
    fd.append('existingImage', existingImage);
    fd.append('newImage', newImage);

    $.ajax({
        url: './controller/updateItemController.php',
        method: 'post',
        data: fd,
        processData: false,
        contentType: false,
        success: function(data) {
            alert('Cập nhật thành công.');
            $('form').trigger('reset'); // Reset form after successful submission
            showProductItems();
        }
    });
}

